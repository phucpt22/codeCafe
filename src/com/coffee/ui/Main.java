    void fillTableNV(){
        String keyword = txtTimKiem.getText();
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);// xoas tất cả các hàng trên JTable
        try {
            List<NhanVien> list = daoNV.selectByKeyword(keyword);// đọc dữ liệu từ CSDL
            for(NhanVien nv : list){
                Object[] row = {nv.getMaNV(),nv.getHoTen(), nv.getNgaySinh(),
                    nv.isVaiTro()?"Quản Lý" : "Nhân viên", nv.getSDT(), nv.getDiaChi(),
                    nv.isGioiTinh()?"Nam" : "Nữ", nv.getMatKhau()
                };
                model.addRow(row);
            }
            if(!Auth.isManager()){
                TableColumnModel tableColumn = tblNhanVien.getColumnModel();
                tableColumn.removeColumn(tableColumn.getColumn(7));
                
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu !");
        }
    }
    
    void setForm(NhanVien nv){
        txtMaNV.setText(nv.getMaNV());
        txtHoTen.setText(nv.getHoTen());
        txtNgaySinh.setDate(nv.getNgaySinh());
        rdoQuanLy.setSelected(nv.isVaiTro());
        rdoNhanVien.setSelected(!nv.isVaiTro());
        txtSoDT.setText(nv.getSDT());
        txtDiaChi.setText(nv.getDiaChi());
        rdoNam.setSelected(nv.isGioiTinh());
        rdoNu.setSelected(!nv.isGioiTinh());
        txtMatKhau.setText(nv.getMatKhau());    
    }
    
    NhanVien getForm(){
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setHoTen(txtHoTen.getText());
        nv.setNgaySinh(txtNgaySinh.getDate());    
        nv.setVaiTro(rdoQuanLy.isSelected());
        nv.setSDT(txtSoDT.getText());
        nv.setDiaChi(txtDiaChi.getText());
        nv.setGioiTinh(rdoNam.isSelected());
        nv.setMatKhau(new String(txtMatKhau.getPassword()));
        return nv;       
    }
    
    public boolean checkTrungMa(JTextField txt) {
        txt.setBackground(white);
        if (daoNV.selectById(txt.getText()) == null) {
            return true;
        } else {
            txt.setBackground(pink);
            MsgBox.alert(this, txt.getName() + " đã bị tồn tại.");
            return false;
        }
    }

    private void timKiem() {
        this.fillTableNV();
        this.clearForm();
        this.row = -1;
    }
    
    void clearForm(){
       NhanVien nv = new NhanVien();
       this.setForm(nv);
       this.row = -1;
    }
    
    void edit(){
        String manv = (String) tblNhanVien.getValueAt(this.row, 0);
        NhanVien nv = daoNV.selectById(manv);
        this.setForm(nv);
    }
    
    void insert(){
            try {
                NhanVien nv = getForm();
                daoNV.insert(nv);
                this.fillTableNV();
                this.clearForm();
                MsgBox.alert(this, "Thêm mới thành công !");
            } catch (Exception e) {
                MsgBox.alert(this, "Thêm mới thất bại !");
            } 
    }
    
    void update(){
        NhanVien nv = getForm();
        try {
            daoNV.update(nv);
            this.fillTableNV();
            MsgBox.alert(this, "Cập nhật thành công !");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại !");
        }
    }
    
    void delete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xóa nhân viên !");
        }
        else{
            String manv = txtMaNV.getText();
            if(manv.equals(Auth.user.getMaNV())){
                MsgBox.alert(this, "Bạn không được xóa chính bạn !");
            }
            else if(MsgBox.confirm(this, "Bạn thực sự muốn xóa nhân viên này ?")){
                try {
                    daoNV.delete(manv);
                    this.fillTableNV();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công !");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại !");
                }
            }
        }
    }
    
